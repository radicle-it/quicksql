// ── Views ─────────────────────────────────────────────────────────────────────

describe('views', () => {

    test('basic view references correct table name with prefix', () => {
        const out = ddl(`dept\n    name\nview v dept\n# settings = {prefix: "abc"}`);
        expect(out.indexOf('from')).toBeLessThan(out.lastIndexOf('abc_dept'));
    });

    test('view column aliases do not leak slash chars', () => {
        const out = ddl(`departments\n    name\n    employees\n       name\nview emp_v departments employees`);
        const trimmed = out.substring(0, out.indexOf('-- Generated by Radicle QuickSQL'));
        expect(trimmed).not.toContain('departments.id/');
    });

    test('reserved word user gets the_ prefix in view alias', () => {
        const out = ddl(`# settings = {"prefix":"IW","pk":"IDENTITY"}\nuser\n    username vc(50)\ntask\n    user_id /fk user\nview task_v task user`);
        expect(out).toContain('iw_user the_user');
        expect(out).toContain('the_user.id');
        expect(out).toContain('the_user.username');
    });

    test('view FROM clause has space between table name and alias', () => {
        const out = ddl(`# settings = {"prefix":"IW","pk":"IDENTITY"}\ndept\n    name\nview dept_v dept`);
        expect(out).toContain('iw_dept dept');
        expect(out).not.toContain('iw_deptdept');
    });

    test('view WHERE clause uses actual FK column name', () => {
        const out = ddl(`# settings = {"prefix":"IW","pk":"IDENTITY"}\nuser\n    username vc(50)\nvisit\n    planned_by /fk user\nview stats_v visit user`);
        expect(out).toContain('left join');
        expect(out).toContain('on visit.planned_by = the_user.id');
        expect(out).not.toContain('(+)');
    });

    test('duplicate column aliases are disambiguated in view', () => {
        const out = ddl(`# settings = {"prefix":"IW","pk":"IDENTITY"}\nuser\n    username vc(50)\nauth\n    user_id /fk user\nview auth_v user auth`);
        expect(out).toContain('auth_user_id');
    });

    test('view without /trans has no LEFT JOIN', () => {
        const out = ddl(`dept\n    name\nview dept_v dept`);
        expect(out).not.toContain('left join');
        expect(out).not.toContain('coalesce');
    });

});

