/**
 * Integration tests for specific DDL behaviors.
 * Ported from small_tests.js — each assertion group becomes a named test case.
 * Organized by feature area, not by bug number.
 */
import { describe, test, expect, beforeEach } from 'vitest';
import quicksql, { toDDL }  from '../../src/ddl.js';
import { resetSeed }         from '../../src/sample.js';

beforeEach(() => { resetSeed(); });

function ddl(input: string, opts?: string): string {
    return new (quicksql as any)(input, opts).getDDL();
}

// ── Settings ──────────────────────────────────────────────────────────────────

describe('settings', () => {

    test('inline #settings takes precedence over constructor parameter', () => {
        const out = toDDL(
            `departments\n    name\n# settings = {"prefix": "RIGHT"}`,
            '{"prefix": "WRONG"}'
        );
        expect(out).toContain('create table right_departments');
    });

    test('unknown setting in constructor parameter raises error message', () => {
        const out = ddl(`Bug35683432\n    name`, '{"notAnOption1": "should raise an Error"}');
        expect(out).toContain('Unknown setting: notanoption1');
    });

    test('unknown setting in inline #settings raises error message', () => {
        const out = ddl(`Bug35683432\n    name\n# settings = {"notAnOption2": "should raise an Error"}`);
        expect(out).toContain('Unknown setting: notanoption2');
    });

    test('overrideSettings prevents parameter from overriding inline settings', () => {
        const out = ddl(
            `Bug35757000\n    name\n# settings = {"overrideSettings":"true"}`,
            '{"prefix":"X"}'
        );
        expect(out).not.toContain('x_');
    });

    test('non-default options appear in output once only', () => {
        const out = ddl(
            `Bug35650456_2\n    job vc5000\n--- Non-default options:\n# settings = {"apex":"Y","db":"19c","longvc":"N"}`
        );
        expect(out.indexOf('Non-default options')).toBe(out.lastIndexOf('Non-default options'));
    });

    test('prefix with trailing underscore does not double the underscore', () => {
        const out = ddl(`dept\n    dname\n# settings = {"prefix": "abc_"}`);
        expect(out).toContain('abc_dept');
        expect(out).not.toContain('abc__dept');
    });

    test('schema + prefix do not generate schema.prefix_id_pk', () => {
        const out = ddl(`dept\n    name\n# settings = {prefix: "prefix", schema: "schema"}`);
        expect(out).not.toContain('schema.prefix_dept_id_pk');
    });

});

// ── Primary keys ──────────────────────────────────────────────────────────────

describe('primary keys', () => {

    test('genpk:false removes identity column', () => {
        const out = ddl(`departments\n    name\n# settings = {"genpk": false}`);
        expect(out.toLowerCase()).not.toContain('id     number generated');
    });

    test('genpk:false via constructor option', () => {
        const out = ddl(`departments\n    name`, '{ "genpk": false }');
        expect(out.toLowerCase()).not.toContain('id     number generated');
    });

    test('pk:NONE — no trigger, explicit PK constraint kept', () => {
        const out = ddl(`bug35748389\n    name\n# settings = {"pk":"NONE"}`);
        expect(out).not.toContain('trigger');
        expect(out).toContain('constraint bug35748389_id_pk primary key,');
    });

    test('pk:SEQ — no trigger, nextval default', () => {
        const out = ddl(`bug35748389_2\n    name\n# settings = {"pk":"seq"}`);
        expect(out).not.toContain('trigger');
        expect(out).toContain('number default on null bug35748389_2_seq.nextval');
        expect(out).toContain('constraint bug35748389_2_id_pk primary key,');
    });

    test('pk:IDENTITY — no trigger, GENERATED BY DEFAULT ON NULL AS IDENTITY', () => {
        const out = ddl(`bug35748389_4\n    name\n# settings = {"pk":"identity"}`);
        expect(out).not.toContain('trigger');
        expect(out).toContain('number generated by default on null as identity');
        expect(out).toContain('constraint bug35748389_4_id_pk primary key,');
    });

    test('genpk:false — no id column at all', () => {
        const out = ddl(`bug35748389_3\n    name\n# settings = {"genpk":false}`);
        expect(out).not.toContain('trigger');
        expect(out).not.toContain('id');
    });

    test('pk:GUID — number with to_number(sys_guid())', () => {
        const out = ddl(`dept\n     dname`);
        expect(out).toContain('number default on null to_number(sys_guid()');
    });

    test('composite PK via /pk col1, col2 — no auto id', () => {
        const out = ddl(`employee /pk first_name, last_name\n    first_name\n    last name\n    job history\n        start_date\n        end_date`);
        expect(out).not.toContain('employee_id');
        expect(out).toContain('alter table employee add constraint employee_pk primary key (first_name,last_name);');
        expect(out).toContain('constraint employee_job_history_fk foreign key (first_name,last_name) references employee;');
    });

    test('composite UK via /UK col1, col2', () => {
        const out = ddl(`employee /UK first_name, last_name\n    first_name\n    last_name`);
        expect(out).toContain('alter table employee add constraint employee_uk unique (first_name,last_name);');
    });

    test('explicit PK column overrides auto id', () => {
        const out = ddl(`person\n    id num /pk\n    name vc40\n    date_of_birth date\n    mother /fk person\n    father /fk person`);
        expect(out).toContain('id               number');
        expect(out).toContain('person_id_pk primary key');
    });

    test('custom PK column name keeps PK constraint', () => {
        const out = ddl(`countries\n    code vc2 /pk`);
        expect(out).toContain('code    varchar2(2 char) not null');
    });

    test('explicit PK column named after table_id', () => {
        const out = ddl(`countries\n    country_id vc2 /pk`);
        expect(out).toContain('country_id    varchar2(2 char) not null');
    });

    test('pk:SEQ — drop sequence included when drop:Y', () => {
        const out = ddl(`# pk: SEQ\n# drop: Y\nstudents\n    name`);
        expect(out).toContain('drop sequence students_seq');
    });

    test('pk:SEQ — on custom pk column', () => {
        const out = ddl(`customers\n    cid /pk\n#settings = { pk: "SEQ"}`);
        expect(out).toContain('cid    number default on null customers_seq.nextval');
        expect(out).not.toContain('trigger');
    });

    test('pk:GUID — no trigger or alter', () => {
        const out = ddl(`# settings = {"pk":"GUID"}\nstudents /insert 2\n    name`);
        expect(out).not.toContain('trigger');
        expect(out).not.toContain('alter');
    });

    test('existing id column is not duplicated', () => {
        const out = ddl(`customers\n    id`);
        expect(out).not.toContain('id    varchar2');
    });

    test('prefixPKwithTname:true applies prefix to PK column names', () => {
        const out = ddl(`# settings = {prefixPKwithTname:true, "api":"Y"}\nperson\n    first_name\n    last_name\n    first_date\naddreess\n    address1\n    address2\n    person_id`);
        expect(out).not.toContain('p_id');
    });

});

