// ── Constraints and directives ────────────────────────────────────────────────

describe('constraints and directives', () => {

    test('/cascade generates ON DELETE CASCADE FK', () => {
        const out = ddl(`dept\n    dname\n    emp /cascade\n        dept_id\n        ename`);
        expect(out).toContain('dept_id    number');
        expect(out).toContain('constraint emp_dept_id_fk');
        expect(out).toContain('references dept on delete cascade');
    });

    test('/setnull generates ON DELETE SET NULL FK', () => {
        const out = ddl(`dept\n    dname\n    emp /setnull\n        dept_id\n        ename`);
        expect(out).toContain('dept_id    number');
        expect(out).toContain('constraint emp_dept_id_fk');
        expect(out).toContain('references dept on delete set null');
    });

    test('/cascade on child table column', () => {
        const out = ddl(`dept\n    dname\n    emp /cascade\n        dept_id\n        ename`);
        expect(out).toContain('constraint emp_dept_id_fk');
        expect(out).toContain('references dept on delete cascade,');
        expect(out).not.toContain('dept_id    integer');
    });

    test('department_id column is not duplicated when child references parent', () => {
        const out = ddl(`departments\n    dname\n    emp\n        department_id /fk departments\n        ename`);
        expect(out.indexOf('department_id    number')).toBe(out.lastIndexOf('department_id    number'));
    });

    test('/nn on non-pk column', () => {
        const out = ddl(`countries\n    code vc2 /pk`);
        expect(out).toContain('code    varchar2(2 char) not null');
    });

    test('/check with quoted values', () => {
        const out = ddl(`Bug35714241\n    proficiency /check 'Test'`);
        expect(out).toContain("in ('Test'");
    });

    test('/check with backtick literals (no double-escaping)', () => {
        const out = ddl("escape /insert 1\n    financial_year /check '23/24', `'24/25'`\n    surname vc60 /check 'O''Hara', q'{O'Tool}'\n    start_date /check `to_date('01-APR-2025','DD-MON-YYYY')`");
        expect(out).toContain("check (financial_year in ('23/24','24/25')),");
        expect(out).toContain("check (surname in ('O''Hara',q'{O'Tool}')),");
        expect(out).toContain("check (start_date in (to_date('01-APR-2025','DD-MON-YYYY')))");
        expect(out).not.toContain("''24/25''");
        expect(out).not.toContain("q''{O''Tool}''");
        expect(out).not.toContain("to_date(''01-APR-2025'',''DD-MON-YYYY'')");
    });

    test('vc(255) maps to varchar2(255 char)', () => {
        const out = ddl(`Bugs35692739\n    description vc(255)`);
        expect(out).toContain('varchar2(255 char)');
    });

    test('/data_type check with comma-separated values', () => {
        const out = ddl(`change_history\n    data_type vc20 /check VARCHAR2,CLOB,TSWLTZ`);
        expect(out).toContain('data_type    varchar2(20');
    });

    test('/domain directive applies domain type on db >= 23', () => {
        const out = ddl(`users\n    email /domain email_d /nn\n    phone /domain phone_d\n# settings = {"db":"23ai"}`);
        expect(out).toContain('email_d not null');
        expect(out).toContain('phone_d');
    });

    test('/domain directive ignored on db < 23', () => {
        const out = ddl(`users2\n    email /domain email_d\n# settings = {"db":"19c"}`);
        expect(out).toContain('varchar2');
        expect(out).toContain('email    varchar2');
    });

    test('int type preserves integer datatype', () => {
        const out = ddl(`test\n    foo_id int /nn /fk foo`);
        expect(out).toContain('foo_id    integer');
    });

});

