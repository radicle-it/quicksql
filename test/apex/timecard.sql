-- create tables

create table employees (
    id               number generated by default on null as identity
                     constraint employees_id_pk primary key,
    name             varchar2(255 char)
                     constraint employees_name_unq unique not null,
    email            varchar2(255 char)
                     constraint employees_email_unq unique not null,
    country          varchar2(100 char) not null,
    employee_type    varchar2(30 char) constraint employees_employee_type_ck
                     check (employee_type in ('full time','part time')),
    approver         varchar2(255 char) not null,
    row_version      integer not null
);

-- table index
create index employees_i1 on employees (approver);

create table timecards (
    id                       number generated by default on null as identity
                             constraint timecards_id_pk primary key,
    employee_id              number                             constraint timecards_employee_id_fk
                             references employees,
    status                   varchar2(4000 char) default on null 'SUBMITTED' constraint timecards_status_ck
                             check (status in ('SUBMITTED','APPROVED','DECLINED')),
    week_of                  date not null,
    sunday                   integer default on null 0 constraint timecards_sunday_bet
           check (sunday between 0 and 24),
    monday                   integer default on null 0 constraint timecards_monday_bet
           check (monday between 0 and 24),
    tuesday                  integer default on null 0 constraint timecards_tuesday_bet
           check (tuesday between 0 and 24),
    wednesday                integer default on null 0 constraint timecards_wednesday_bet
           check (wednesday between 0 and 24),
    thursday                 integer default on null 0 constraint timecards_thursday_bet
           check (thursday between 0 and 24),
    friday                   integer default on null 0 constraint timecards_friday_bet
           check (friday between 0 and 24),
    saturday                 integer default on null 0 constraint timecards_saturday_bet
           check (saturday between 0 and 24),
    comments                 varchar2(4000 char),
    confirmation_code_v50    varchar2(4000 char),
    row_version              integer not null,
    created                  date not null,
    created_by               varchar2(255 char) not null,
    updated                  date not null,
    updated_by               varchar2(255 char) not null
);

-- table index
create index timecards_i1 on timecards (employee_id);




-- triggers
create or replace trigger employees_bi
    before insert
    on employees
    for each row
begin
    :new.email := lower(:new.email);
    :new.row_version := 1;
end employees_bi;
/

create or replace trigger employees_bu
    before update
    on employees
    for each row
begin
    :new.email := lower(:new.email);
    :new.row_version := nvl(:old.row_version, 0) + 1;
end employees_bu;
/


create or replace trigger timecards_bi
    before insert
    on timecards
    for each row
begin
    :new.row_version := 1;
    :new.created := sysdate;
    :new.created_by := coalesce(sys_context('APEX$SESSION','APP_USER'),user);
    :new.updated := sysdate;
    :new.updated_by := coalesce(sys_context('APEX$SESSION','APP_USER'),user);
end timecards_bi;
/

create or replace trigger timecards_bu
    before update
    on timecards
    for each row
begin
    :new.row_version := nvl(:old.row_version, 0) + 1;
    :new.updated := sysdate;
    :new.updated_by := coalesce(sys_context('APEX$SESSION','APP_USER'),user);
end timecards_bu;
