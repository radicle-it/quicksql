create or replace package body employees_api
is

    procedure get_row (
        p_id        in  integer,
        p_name   out  varchar2,
        p_email   out  varchar2,
        p_country   out  varchar2,
        p_employee_type   out  varchar2,
        p_approver   out  varchar2
    )
    is 
    begin 
        select name,
               email,
               country,
               employee_type,
               approver
          into p_name,
               p_email,
               p_country,
               p_employee_type,
               p_approver
          from employees
         where id = p_id;
    exception
        when no_data_found then
            null;
    end get_row;
 
    procedure insert_row (
        p_id        in  integer default null,
        p_name    in  varchar2 default null,
        p_email    in  varchar2 default null,
        p_country    in  varchar2 default null,
        p_employee_type    in  varchar2 default null,
        p_approver    in  varchar2 default null
    )
    is 
    begin 
        insert into employees ( 
            id,
            name,
            email,
            country,
            employee_type,
            approver
        ) values ( 
            p_id,
            p_name,
            p_email,
            p_country,
            p_employee_type,
            p_approver
        );
    end insert_row;
 
     procedure update_row (
        p_id        in  integer default null,
        p_name    in  varchar2 default null,
        p_email    in  varchar2 default null,
        p_country    in  varchar2 default null,
        p_employee_type    in  varchar2 default null,
        p_approver    in  varchar2 default null
    )
    is 
    begin 
        update  employees set 
            id = p_id,
            name = p_name,
            email = p_email,
            country = p_country,
            employee_type = p_employee_type,
            approver = p_approver
        where id = p_id;
    end update_row;
 
     procedure delete_row (
        p_id              in integer
    )
    is
    begin
        delete from employees where id = p_id;
    end delete_row;
end employees_api;
